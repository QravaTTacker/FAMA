"""
1) Determine quantos metros quadrados de grama são necessários para preencher
uma praça circular com raio de 20 metros.
"""
# Declaração da variável pi
pi = 3.14159
# Declaração da variável raio
r = 20
# Realiza o cálculo da área do círculo
area = pi*(r**2)
# Mostra o resultado do cálculo
print("Total da área: ", area, "metros quadrados")
