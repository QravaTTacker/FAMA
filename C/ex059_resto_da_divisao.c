#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Resto de uma divisão é a parte que sobra, exemplo: 5/2 = seu resto é 1
    // a representação de resto de uma divisão se dá pelo símbolo de porcentagem %
    // Resto de uma divisão é um número natural, ou seja, de tipo primitivo int.

    printf("%i\n", 5/2); // Só irá imprimir 2

    printf("%i\n", 5%2); //Só irá imprimir 1

    return 0;
}
